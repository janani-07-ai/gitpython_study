# Bitwise operators
'''
&   AND
|   OR
^   XOR
~   NOT
<<  Zero fill left shift
>>  Signed right shift

'''
a=25
b=45
print(a & b)#two binarys are same only as 1 then true
print(a | b)#different binarys then 1 
print(a ^ b)#different binary is 1 same binary is 0
print(~a)#add one no after that and aading - in before
print(b << 2)#two binary in left goes and add on right side
print(b >> 2)#two binary in right goes and removed





