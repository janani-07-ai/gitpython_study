#assignment operators

'''
=   Assignment
+=  Addition
-=  Subtraction
*=  Multiplication
/=  Division
%=  Modulus
**= exponentiation
//= Floor division

'''
a=600  #assignment 
a+=5 #addition + assignment a=a+5
print(a)
a-=10#a=a-10
print(a)
a*=10#a=a*10
print(a)
a/=10#a=a/10
print(a)
a%=10#a=a%10
print(a)
a//=10#a=a//10
print(a)
a**=10#a=a**10
print(a)
